public class LinkedNode {


    private int data;
    private LinkedNode next;

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public LinkedNode getNext() {
        return next;
    }

    public void setNext(LinkedNode node) {
        this.next = node;
    }

}
